export function getRepo2SiteDarkThemeMood(themeId: string): Repo2SiteDarkThemeMood {
  switch (themeId) {
    case "saas-clean":
      return MONO_INK_MOOD;
    case "systems-green":
    case "terminal-lime":
      return FOREST_EMERALD_MOOD;
    case "editorial-amber":
    case "creator-orange":
      return OBSIDIAN_AMBER_MOOD;
    case "signal-violet":
      return VIOLET_GRAPHITE_MOOD;
    case "soft-rose":
    case "neon-coral":
      return ROSE_PLUM_MOOD;
    case "ocean-cyan":
    case "pastel-sky":
      return MIDNIGHT_CYAN_MOOD;
    case "slate-mono":
    case "contrast-ink":
      return MONO_INK_MOOD;
    case "midnight-steel":
    case "builder-blue":
    default:
      return BLUE_SLATE_MOOD;
  }
}
