export function getPortfolioThemeById(themeId: string) {
  return PORTFOLIO_THEMES.find((theme) => theme.id === themeId) ?? PORTFOLIO_THEMES[0];
}

export function applyCustomPaletteToTheme(
  theme: PreviewTheme,
  customPalette?: Partial<PreviewTheme["palette"]>,
): PreviewTheme {
  if (!customPalette) {
    return theme;
  }

  const nextPalette = {
    ...theme.palette,
    ...Object.fromEntries(
      Object.entries(customPalette).filter(([, value]) => Boolean(value && String(value).trim())),
    ),
  };

  return {
    ...theme,
    palette: nextPalette,
  };
}
